package com.dunhili.eternitysdungeon.character;

/**
 * Enumerated type for the different genders a character can be.
 * Created by Dunhili on 5/23/2015.
 */
public enum Gender {
    /** Male character. */
    MALE,
    /** Female character. */
    FEMALE,
    /** Gender that is not male or female. */
    OTHER,
    /** Gender-less character. */
    NONE
}
